import { type ReactNode } from 'react'
import { cn } from '@/lib/utils/cn'

interface SectionProps {
  children: ReactNode
  className?: string
  variant?: 'default' | 'dark' | 'dark-alt'
}

export function Section({ children, className, variant = 'default' }: SectionProps) {
  const variants = {
    default: 'bg-neutral-50 text-neutral-900',
    dark: 'bg-navy-950 text-white',
    'dark-alt': 'bg-navy-900 text-white'
  }

  return (
    <section className={cn('py-20 lg:py-32', variants[variant], className)}>
      <div className="container mx-auto px-4 sm:px-6 lg:px-8 max-w-7xl">
        {children}
      </div>
    </section>
  )
}




















































































































































































































